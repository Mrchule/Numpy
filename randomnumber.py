import numpy as np

# Generate a random 1D array with 5 elements
random_array_1d = np.random.rand(5)

# Generate a random 2D array with shape (3, 4)
random_array_2d = np.random.rand(3, 4)

# Generate a random 3D array with shape (2, 3, 2)
random_array_3d = np.random.rand(2, 3, 2)

print("Random 1D array:")
print(random_array_1d)

print("\nRandom 2D array:")
print(random_array_2d)

print("\nRandom 3D array:")
print(random_array_3d)
